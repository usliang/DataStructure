public class NodeWithMin {
    private int min;
    private int data;

    public NodeWithMin(int min, int data) {
        this.min = min;
        this.data = data;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }
}
